USE PHARMACY
GO


CREATE TABLE ADMIN (
	A_ID VARCHAR(10) FOREIGN KEY (A_ID) REFERENCES EMPLOYEE(EMP_ID),
	A_USERNAME VARCHAR(50) PRIMARY KEY,
	A_PASSWORD VARCHAR(50) NOT NULL,
);	
GO

CREATE TABLE EMPACCOUNT (
	EMPAC_ID VARCHAR(10) FOREIGN KEY REFERENCES EMPLOYEE(EMP_ID),
	EMPAC_USERNAME VARCHAR(50) PRIMARY KEY,
	EMPAC_PASSWORD VARCHAR(50) NOT NULL,
);
GO

CREATE TABLE CUSTOMER (
	C_ID VARCHAR(10) PRIMARY KEY,
	C_NAME NVARCHAR(50) NOT NULL,
	C_SEX NVARCHAR(3) CHECK (C_SEX IN('Nam',N'Nữ')) NOT NULL,
	C_DATE_OF_BIRTH DATE NOT NULL,
	C_PHONE CHAR(10) UNIQUE NOT NULL,
	C_MAIL VARCHAR(50) DEFAULT NULL UNIQUE,
);
GO

CREATE TABLE EMPLOYEE (
	EMP_ID VARCHAR(10) PRIMARY KEY,
	EMP_NAME NVARCHAR(100) NOT NULL,
	EMP_SEX NVARCHAR(3) CHECK (EMP_SEX IN('Nam',N'Nữ')) NOT NULL, 
	EMP_DATE_OF_BIRTH DATE NOT NULL,
	EMP_TYPE VARCHAR(10) CHECK (EMP_TYPE IN ('ADMIN', 'MANAGER', 'PHARMACIST')) NOT NULL,
	EMP_JOB_DATE DATE NOT NULL,
	EMP_PHONE CHAR(10) NOT NULL UNIQUE,
	EMP_MAIL VARCHAR(50) UNIQUE DEFAULT NULL,
	EMP_STORE NVARCHAR(50) FOREIGN KEY (EMP_STORE) REFERENCES SUPPLIERS(SUP_ADD),
);	
GO

CREATE TABLE MEDICINES (
	MED_BARCODE DECIMAL(13,0) UNIQUE NOT NULL,
	MED_ID DECIMAL(6,0) PRIMARY KEY,
	MED_NAME VARCHAR(50) NOT  NULL,
	MED_QTY INT NOT NULL,
	MED_UNIT NVARCHAR(20) CHECK (MED_UNIT IN (N'Vỉ', N'Hộp', N'Thùng')) NOT NULL,
	MED_CATEGORY NVARCHAR(50) NOT NULL,
	MED_PRICE DECIMAL(13,4) NOT NULL,
	MED_STATUS NVARCHAR(20) CHECK (MED_STATUS IN (N'Còn hàng', N'Hết hàng')) NOT NULL,	
);
GO

CREATE TABLE PURCHASE (
	P_ID DECIMAL(6,0),
	SUP_ID VARCHAR(6) NOT NULL,
	MED_ID DECIMAL(6,0) NOT NULL,
	P_QTY INT NOT NULL,
	P_UNIT NVARCHAR(20) CHECK (P_UNIT IN (N'Vỉ', N'Hộp', N'Thùng')) NOT NULL,
	P_COST DECIMAL(11,4) NOT NULL,
	PUR_DATE DATE NOT NULL,
	MFG_DATE DATE NOT NULL,
	EXP_DATE DATE NOT NULL,
	P_STATUS NVARCHAR(20) CHECK (P_STATUS IN (N'Đã giao', N'Chưa giao')) NOT NULL,

	PRIMARY KEY (P_ID,MED_ID),
	FOREIGN KEY (SUP_ID) REFERENCES SUPPLIERS(SUP_ID),
	FOREIGN KEY (MED_ID) REFERENCES MEDICINES(MED_ID),
);
GO

CREATE TABLE SALES (
	S_ID INT PRIMARY KEY,
	S_DATE DATE NOT NUll,
	S_TIME TIME NOT NULL,
	C_ID VARCHAR(10) NOT NULL FOREIGN KEY (C_ID) REFERENCES CUSTOMER(C_ID),
	S_AMT DECIMAl(20,2) DEFAULT NULL,
	EMP_ID VARCHAR(10) NOT NULL FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE(EMP_ID),
);
GO

CREATE TABLE SALES_ITEMS (
	S_ID INT NOT NULL,
	MED_ID decimal(6,0) NOT NULL,
	ST_QTY INT NOT NULL,
	ST_UNIT NVARCHAR(20) CHECK (ST_UNIT IN (N'Vỉ', N'Hộp', N'Thùng')) NOT NULL,
	TOTAL_PRICE decimal(20,2) NOT NULL,

	 PRIMARY KEY (S_ID, MED_ID),
	 FOREIGN KEY (S_ID) REFERENCES SALES(S_ID),
	constraint ST_MED_ID_FK FOREIGN KEY (MED_ID) REFERENCES MEDICINES(MED_ID),
);
GO

